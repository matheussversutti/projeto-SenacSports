     private  void setar_campos(){
         int setar = DadosCliente.getSelectedRow();
           
            txtNome.setText(DadosCliente.getModel().getValueAt(setar, 1).toString());
            txtEndereco.setText(DadosCliente.getModel().getValueAt(setar, 2).toString());
            txtEmail.setText(DadosCliente.getModel().getValueAt(setar, 3).toString());
            cmdSexo.setSelectedItem(DadosCliente.getModel().getValueAt(setar, 4).toString());
            txtTelefone.setText(DadosCliente.getModel().getValueAt(setar, 5).toString());
            cmdEC.setSelectedItem(DadosCliente.getModel().getValueAt(setar, 6).toString());
            txtData.setText(DadosCliente.getModel().getValueAt(setar, 7).toString());
            
           
           PreencherTabela();
    }
     
     private void PreencherTabela(){
         ClienteDAO clienteDAO = new ClienteDAO();
        
         List<Cliente> listaCadastro_Cliente = clienteDAO.listar();
         
         DefaultTableModel TabelaClientes = (DefaultTableModel) DadosCliente.getModel();
         TabelaClientes.setNumRows(0);
         for(Cliente c : listaCadastro_Cliente)
        {
        Object[] obj = new Object[]{
            c.getCpf(),
            c.getNome(),
            c.getEndereco(),
            c.getEmail(),
            c.getSexo(),
            c.getFone(),
            c.getEstado_civil(),
            c.getData_nascimento()
        };   
         TabelaClientes.addRow(obj);
     }
     }
     

