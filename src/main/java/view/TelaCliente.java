    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
       String  cpf = txtCpfConsulta.getText(); 
       ClienteDAO ClienteDAO  = new ClienteDAO();
       
       Cliente cliente = ClienteDAO.Consultar(cpf);
       
       
           PreencherTabela();
       
       
       
       if(cliente == null){
           JOptionPane.showMessageDialog(this, "Cliente não cadastrado! ");
       }
       else{
          
         

            DefaultTableModel TabelaClientes = (DefaultTableModel) DadosCliente.getModel();
            TabelaClientes.setRowCount(0);
            
            TabelaClientes.addRow(new String[]{String.valueOf(cliente.getCpf()),
                                        String.valueOf(cliente.getNome()),
                                        String.valueOf(cliente.getEndereco()),
                                        String.valueOf(cliente.getEmail()),
                                        String.valueOf(cliente.getSexo()),
                                        String.valueOf(cliente.getFone()),
                                        String.valueOf(cliente.getEstado_civil()),
                                        String.valueOf(cliente.getData_nascimento())});
         
       }
    }//GEN-LAST:event_btnPesquisarActionPerformed
    
